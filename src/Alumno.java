public class Alumno {
    String carnet;
    String nombres;
    String apellidos;
    String seccion;
    double nota;
    public Alumno(String carnet, String nombres, String apellidos, String seccion, double nota) {
        this.carnet = carnet;
        this.nombres = nombres;
        this.apellidos = apellidos;
        this.seccion = seccion;
        this.nota = nota;
    }
}